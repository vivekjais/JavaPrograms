//WAP Print 3 Different Range Using For Loop
package regularClassStudyAssinment;
public class Assignment11 
{
  public static void main(String[] args) 
  {
	System.out.println(".........................");
	System.out.println("First Loop Started form 10");
	for(int i=10;i<=40;i++)
	{
		System.out.println(i);
	}
	System.out.println("First Loop Ended at 40");
	System.out.println(".........................");
	  
	System.out.println("Second Loop Started form -10");
	for(int i=-10;i>=-40;i--)
	{
	
		System.out.println(i);
	}
	System.out.println("Second Loop Ended at -40");
	System.out.println(".........................");
	
	System.out.println("Third Loop Started form -10");
	for(int i=-10;i<=40;i++)
	{
		System.out.println(i);
	}
	System.out.println("Third Loop Ended at 40");
	System.out.println(".........................");
  }
}
